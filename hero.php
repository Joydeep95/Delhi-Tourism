<div id="text">Virat Kohli:<br>

 Virat Kohli is a cricketer a middle-order batsman, of the Indian team. He was born in Delhi and attended school at Vishal Bharti and Savier Convent. Kohli was the captain of the victorious Indian team at the 2008 U/19 Cricket World Cup held in Malaysia.

He represents Delhi in first-class cricket and is the captain of the Royal Challengers Bangalore franchise in the Indian Premier League. He also played for the West Delhi Cricket Academy.

Kohli made his One Day International (ODI) debut in 2008 and was part of the Indian team which won the 2011 World Cup. Despite being a regular in the ODI side, Kohli only played his first Test in 2011 against West Indies in Kingston. But on the disastrous 2011/12 India tour of Australia, in which India's senior batsmen struggled throughout, Kohli stood out, scoring his first Test hundred in Adelaide.

Kohli was the recipient of the ICC ODI Player of the Year award in 2012. Sports Pro has rated him the 13th most marketable athlete in the world.
</div><br>
<div id="text">Virender Sehwag:<br>

 Virender Sehwag is one of the leading batsmen in the Indian cricket team. His family hails from Haryana; he is settled in New Delhi. He attended Arora Vidya School in Delhi, later he attended Jamia Milia Islamia for graduation.

Sehwag is an aggressive right-handed opening batsman and a part-time right-arm off-spin bowler. Sehwag became the only Indian to be honoured as the Wisden Leading Cricketer in the World for his performance in 2008, subsequently becoming the first player of any nationality to retain the award for 2009.

Sehwag holds multiple records including the highest score made by an Indian in Test cricket (319), which was also the fastest triple century in the history of international cricket (reached 300 off only 278 balls) as well as the fastest 250 by any batsman (in 207 balls against Sri Lanka on 3 December 2009 at the Brabourne Stadium in Mumbai). His other innings of 309 and 293 are also the second and third best by any Indian player.

Sehwag also holds the distinction of being one of four batsmen in the world to have ever surpassed 300 twice in Test cricket, and the only one to score two triple centuries and take a five-wicket innings haul. He is the only player in world to score a double hundred in ODI and a triple hundred in Test Cricket.</div><br>
