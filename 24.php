<div id="intro">The 10 Things You Should Do, If You Have Just 24 Hours In Delhi</div>

<div id="text"><img src="images/lodhi garden.jpg" width="50%"><br>1. Start Your Day With A Walk At Lodhi Gardens<br>
When you are in Delhi for day ten start your day with a morning walk in Lodhi Garden. This place has historical value and you might get to see celebs, bureaucrats, industrialist, and leaders walking and running in this garden. it is one of the cleanest and well maintained garden and the best time is morning from 7 to 8:30 am.
</div><br>
<div id="text">2. Skip The Boring Hotel Breakfast And Head To The Impressive Jama Masjid<br>
 The best time to explore Delhi is in the morning when the climate is cool. Jama Masjid is one of the famous places in Delhi and people come to this area to eat something exotic. When you are here, just enjoy your meal and don’t get bothered about the crowd. Never ignore nihari breakfast at Karims and Al jawahar at Jama Masjid.
</div><br>
<div id="text">3. Watch The Colossal Red Sandstone Monument - Red Fort<br>
 Red fort is must visit places in Delhi, where you get to see beautiful museum inside it. The wall of red fort is beautifully crafted and painted. You just need to pay 15 Rs. only to visit this incredible place and 15 Rs also includes a visit to the museum.
</div><br>
<div id="text">4. Spend Your Afternoon Checking Out Gorgeous Works At National Gallery Of Modern Art<br>
 If you are an art lover and want to spend some quality time in Delhi then National Gallery Of Modern Art is the perfect place. Here you get to see some wonderful portraits; self-portraits which were completely fascinated you with its sensuality and emotion. You can enjoy this art gallery in juts 10 Rs.
</div><br>
<div id="text">5. Gorge On Local Food At Chandni Chowk<br>
 Local food o street food is just awesome in Delhi, to enjoy some real delicious food you can travel to Paratha wali gali, Amritsari lassi wala, Kakke di haati. Chandni Chowk is also famous among foodies, and you must visit this place also. Walk in busy roads and street to enjoy the real taste of India.
</div><br>
<div id="text">6. Take A Rickshaw Ride To Connaught Place<br>
 Connaught place is shopping destination and the rickshaw ride is famous here. When you’re in Delhi then do taste the ride in a rickshaw. This place is for Shopaholics where you will get a huge variety of shops and you can shop for Handicraft and here you can spend time with friends in an awesome park.
</div><br>
<div id="text">7. Take A Selfie At India Gate<br>
 India Gate is famous and historic place in Delhi where you can take selfies and enjoy the evening with friends and family. In the evening India gate looks awesome with Colorful floodlight. India gate is pride not only in Delhi but for India.
</div><br>
<div id="text">8. Lose Yourself To Shopping At Dilli Haat<br>
 Dili Haat is a place where you will get to see the every culture and color of India. Here you can buy Handicraft from handicraft stall showing art from all over India. You should get items like rosewood for sure.  One can’t resist but buy embellished camel hide footwear. You can also buy metal crafts, silk, wood, various fabrics, and artifacts.
</div><br>
<div id="text">9. Spend The Evening At The Cafes At Hauz Khas<br>
 When you are in Delhi you must visit cafes in Hauz Khas Village and it is the most happening place to be in Delhi. the Kunzum Travel Cafe is one of the best café and here you will get unique concept “Pay what you like” concept.
</div><br>
<div id="text">10. Drive Down To Murthal via NH1<br>
If you want some adventure, then you must drive on NH1 and drive toll tax stop on the way. Here you can get plenty of food options. Road conditions here are just good so will enjoy the trip.
</div><br>
